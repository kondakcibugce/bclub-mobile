import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const htmlPath = path.join(root, 'bchallenge', 'Bugce Challenge.html');
let html = await readFile(htmlPath, 'utf8');

html = html.replace(
  /<meta name="viewport" content="width=1280" \/>/,
  '<meta name="viewport" content="width=device-width, initial-scale=1, viewport-fit=cover" />'
);

html = html.replace('</head>', `<script>
  window.__BCLUB_MOBILE__ = true;
</script>
<style>
  html, body, #app {
    width: 100%;
    height: 100%;
    min-height: 100%;
    overflow: hidden;
    background: #fdfaf3;
  }
</style>
</head>`);

const scriptSrcPattern = /<script type="text\/babel" src="([^"]+)"><\/script>/g;
html = await replaceAsync(html, scriptSrcPattern, async (_match, src) => {
  const script = await readFile(path.join(root, 'bchallenge', src), 'utf8');
  return `<script type="text/babel">\n${script}\n</script>`;
});

await mkdir(path.join(root, 'src'), { recursive: true });
await writeFile(
  path.join(root, 'src', 'appHtml.js'),
  `// Generated by scripts/build-app-html.mjs. Do not edit by hand.\nexport const html = ${JSON.stringify(html)};\n`
);

async function replaceAsync(str, regex, asyncFn) {
  const promises = [];
  str.replace(regex, (...args) => {
    promises.push(asyncFn(...args));
    return '';
  });
  const data = await Promise.all(promises);
  return str.replace(regex, () => data.shift());
}
